# Comparison operators in Python

"""
1. ==
2. !=
3. >
4. <
5. >=
6. <=

"""

# Example:
x = 10
y = 12

print(id(x),type(x))

print(id(y),type(y))

# Output: x > y is False
print('x > y  is',x>y)

# Output: x < y is True
print('x < y  is',x<y)

# Output: x == y is False
print('x == y is',x==y)

# Output: x != y is True
print('x != y is',x!=y)

# Output: x >= y is False
print('x >= y is',x>=y)

# Output: x <= y is True
print('x <= y is',x<=y)
